import gc
import torch
from transformers import AutoTokenizer, AutoModelForCausalLM, BitsAndBytesConfig

MODEL_NAME = "defog/sqlcoder-7b-2"

# Clear GPU memory
gc.collect()
torch.cuda.empty_cache()

# Load tokenizer
tokenizer = AutoTokenizer.from_pretrained(
    MODEL_NAME,
    trust_remote_code=True
)

# 4-bit quantization config
bnb_config = BitsAndBytesConfig(
    load_in_4bit=True,
    bnb_4bit_compute_dtype=torch.float16,
    bnb_4bit_use_double_quant=True,
    bnb_4bit_quant_type="nf4"
)

# Load model
model = AutoModelForCausalLM.from_pretrained(
    MODEL_NAME,
    quantization_config=bnb_config,
    device_map={"": 0},
    trust_remote_code=True
)

model.eval()


def generate_sql(prompt):
    inputs = tokenizer(
        prompt,
        return_tensors="pt",
        truncation=True,
        max_length=4096
    ).to(model.device)

    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            max_new_tokens=80,
            do_sample=False,
            pad_token_id=tokenizer.eos_token_id
        )

    result = tokenizer.decode(outputs[0], skip_special_tokens=True)

    if "SQL:" in result:
        sql = result.split("SQL:")[-1].strip()
    else:
        sql = result.strip()

    # clean markdown if model adds it
    sql = sql.replace("```sql", "").replace("```", "").strip()

    return sql

def run_pipeline(question):
    print("\nUser Question:")
    print(question)

    tables = load_metadata("matadata.json")

    selected_tables = select_top_tables(question, tables, top_k=3)

    print("\nTop 3 Selected Tables:")
    for table in selected_tables:
        print(f"- {table['table_name']} | score: {table['score']:.4f}")

    schema_text = build_schema_text(selected_tables)

    print("\nSelected Schema:")
    print(schema_text)

    prompt = build_prompt(question, schema_text)

    sql = generate_sql(prompt)

    print("\nGenerated SQL:")
    print(sql)

    return sql


if __name__ == "__main__":
    question = input("Ask your question: ")
    run_pipeline(question)